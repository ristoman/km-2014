<p><strong>For how long have been in photography?</strong> 12 years</p>

<p><strong>How did you start?</strong> Working as a Photographers Assistant and Photographic printer in London for several years</p>

<p><strong>How would you define yourself as an artist?</strong> A Photographer with quite a clean, graphic style of shooting who loves to use natural and ambient light as much as possible.</p>

<p><strong>Where do you find inspiration?</strong> Mainly through traveling meeting new people and experiencing new cultures, traditions and styles. </p>

<p><strong>Is Brainstorming not the only creative method use to create new concepts?</strong> Brainstorming and experimentation - trial and error is great its always worth pushing the boundaries and going against the rules to see what happens</p>

<p><strong>Please could you tell us about photography and digital technology?</strong> The proliferation of the internet and digital technology has made my life so much easier. I travel so much with work it allows me to get my images to clients all over the world so much quicker than ever before. Social media and websites have also opened up new areas of image usage and the ability to promote my work so quickly and internationally it's fantastic.</p>

<p><strong>What type of camera do you use most?</strong> Nikon D800</p>

<p><strong>What is your favorite lens?</strong> 85mm</p>

<p><strong>What has been your most memorable assignment and why?</strong> Shooting a fashion Editorial for Net a Porter in Place De la Concorde in Paris the weather was terrible up until 4pm then it completely changed and we had to quickly turn the shoot around in 2 hours dodging the traffic and the tourists!</p>

<p><strong>What are your favorite three images you have shot recently?</strong> Kate Moss in a Louis Vuitton catwalk show in Paris, Model Hanneli Mustaparta in Paris for a fashion editorial for Net a Porter and a family portrait of my Mum and Dad.</p>

<p><strong>How important is an awesome website for your business?</strong> Incredibly important a great website should represent your unique style as a photographer therefore inspiring potential clients to book you for work. </p>

<p><strong>What’s the most important quality a photographer needs to have?</strong> The ability to remain calm and organized in all situations </p>

